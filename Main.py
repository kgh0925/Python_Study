import turtle as t
def box(pos_x,pos_y,x,y,color) :
  t.speed('fast') # slowest,slow,normal,fast,fastest
  t.penup()
  t.goto(pos_x,pos_y)
  t.pendown()
  t.color(color)
  t.begin_fill()
  t.fd(x)
  t.left(90)
  t.fd(y)
  t.left(90)
  t.fd(x)
  t.left(90)
  t.fd(y)
  t.end_fill()
  t.left(90)

